    <h1 class="mb-4 text-center">Sejarah SMKN 2 Buduran</h1>

    <p>
      <strong>SMK Negeri 2 Buduran</strong> berlokasi di <strong>Jl. Jenggolo No.2A, Siwalanpanji, Kecamatan Buduran, Kabupaten Sidoarjo, Jawa Timur</strong>.
      Sekolah ini didirikan pada tahun <strong>1985</strong> dengan nama awal <strong>STM Negeri Buduran</strong>.
      Sejak awal berdiri, sekolah ini menjadi salah satu pelopor pendidikan kejuruan di Sidoarjo dengan tujuan mencetak tenaga kerja terampil sesuai kebutuhan industri.
    </p>

    <p>
      Kepala sekolah pertama adalah <strong>Bapak Soedarmo</strong>, yang menjadi figur penting dalam membentuk budaya sekolah, membangun jaringan kerja sama industri, dan memperkenalkan kedisiplinan vokasional sejak awal.
    </p>

    <p>
      Pada awal operasional, STM Buduran hanya memiliki beberapa jurusan teknik, seperti Teknik Elektro dan Teknik Mesin.
      Seiring berkembangnya kebutuhan industri dan dunia kerja, program keahlian terus diperbarui dan dikembangkan.
    </p>

    <p>
      Pada tahun <strong>2005</strong>, berdasarkan regulasi pendidikan nasional, STM Buduran resmi bertransformasi menjadi <strong>SMK Negeri 2 Buduran</strong>.
      Perubahan ini membawa penyesuaian kurikulum, sistem pembelajaran berbasis kompetensi, serta penambahan jurusan non-teknik untuk merespons kebutuhan pasar kerja yang lebih luas.
    </p>

    <p>
      Saat ini, SMKN 2 Buduran menjadi sekolah kejuruan unggulan dengan 6 kompetensi keahlian aktif yang terus berkembang:
    </p>

    <div class="row">
      <div class="col-md-6">
        <ul>
          <li><strong>Bisnis Digital(BD)</strong></li>
          <li><strong>Akuntansi Keuangan(AK)</strong></li>
          <li><strong>Rekayasa Perangkat Lunak (RPL)</strong></li>
          <li><strong>Manajemen Perkantoran (MP)</strong></li>
          <li><strong>Layanan Perbankan (LPb)</strong></li>
          <li><strong>Desain Komunikasi Visual (DKV)</strong></li>
        </ul>
      </div>
    </div>

    <p>
      SMKN 2 Buduran juga aktif dalam program pemerintah seperti <strong>SMK Pusat Keunggulan (SMK PK)</strong>,
      serta bekerja sama dengan dunia industri melalui program <strong.,>Teaching Factory (TeFa)</strong>, Praktek Kerja Lapangan (PKL), dan sertifikasi kompetensi.
    </p>

    <p>
      Selain prestasi akademik, sekolah ini juga dikenal memiliki banyak capaian di bidang non-akademik, baik di tingkat regional maupun nasional, melalui kegiatan lomba siswa, ekstrakurikuler, dan inovasi wirausaha pelajar.
    </p>

    <p>
      Saat ini, SMKN 2 Buduran dipimpin oleh <strong>Ibu Dra. Hj. Mariya Ernawati, M.M.</strong>,
      yang meneruskan komitmen pengembangan mutu sekolah, peningkatan SDM guru, serta digitalisasi sistem pembelajaran dan manajemen sekolah.
    </p>

